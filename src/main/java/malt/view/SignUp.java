    private boolean isValidEmail(String email) {
    // Regular expression for standard email format
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
    return email.matches(emailRegex);
    }
    
    
    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
        // TODO add your handling code here:
        // Get the entered email and password
        String email = NewUserEmail.getText();
        String password = String.valueOf(NewUserPassword.getPassword());

        // Validate email format
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address.");
            return;
        }

        // Validate password length
        if (password.length() < 8) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 characters long.");
            return;
        }
        
        // Check if the email is already registered
        FindIterable<Document> result = collection.find(Filters.eq("email", email));
        if (result.iterator().hasNext()) {
            JOptionPane.showMessageDialog(this, "Email already registered. Please contact customer care for assistance.");
            return;
        }

        // If email and password are valid, proceed with insertion
        Document d = new Document("name", UserFullName.getText())
                        .append("email", email)
                        .append("password", password)
                        .append("projectIds", new ArrayList<>()); // Initialize empty project ID list
        collection.insertOne(d);
        JOptionPane.showMessageDialog(this, "Regestration successful");
        System.out.println("User Registered Successfully");
        
        // Dispose the sign-up page and navigate to the login page
        Login LoginFrame = new Login();
        LoginFrame.setVisible(true);
        LoginFrame.pack();
        LoginFrame.setLocationRelativeTo(null); 
        this.dispose();
    }//GEN-LAST:event_SignUpBtnActionPerformed

