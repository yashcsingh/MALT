    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Get the entered email and password
        String email = UserEmail.getText();
        String password = String.valueOf(UserPassword.getPassword());

        // Find the user document with the entered email
        FindIterable<Document> result = collection.find(Filters.eq("email", email));

        // Check if there is a user with the entered email
        if (result.iterator().hasNext()) {
            // User found, now check the password
            Document userDoc = result.first();
            String storedPassword = userDoc.getString("password");
            

            if (password.equals(storedPassword)) {
                // Password matches, login successful
                System.out.println("Login Successful");
                // Dispose the login page and navigate to the Home Page
                
                // Set the user's ObjectId and email in the session
                UserSession session = UserSession.getInstance();
                session.setUserEmail(email);
                session.setUserObjectId(userDoc.getObjectId("_id").toString());
                
                Home homeFrame = new Home();
                homeFrame.setVisible(true);
                homeFrame.pack();
                homeFrame.setLocationRelativeTo(null);
                this.dispose();
            } else {
                // Password does not match
                System.out.println("Incorrect Password");
                JOptionPane.showMessageDialog(this, "Incorrect Password");
                // You can display an error message to the user
            }
        } else {
            // User not found
            System.out.println("User not found");
            JOptionPane.showMessageDialog(this, "Email not Registered");
            // You can display an error message to the user
        }
    }//GEN-LAST:event_jButton1ActionPerformed

